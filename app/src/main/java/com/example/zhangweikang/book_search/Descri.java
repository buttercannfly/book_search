package com.example.zhangweikang.book_search;


import java.util.ArrayList;

public class Descri {
    public String img;
    public String price;
    public ArrayList<String> kinds;
    public ArrayList size;
    public ArrayList specific;
    public String dict;
    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getPrice() {
        return price;
    }
    public void setPrice(String price) {
        this.price = price;
    }

    public ArrayList getKinds() {
        return kinds;
    }

    public void setKinds(ArrayList kinds) {
        this.kinds = kinds;
    }

    public ArrayList getSize() {
        return size;
    }

    public void setSize(ArrayList size) {
        this.size = size;
    }

    public ArrayList getSpecific() {
        return specific;
    }

    public void setSpecific(ArrayList specific) {
        this.specific = specific;
    }

    public String getDict() {
        return dict;
    }

    public void setDict(String dict) {
        this.dict = dict;
    }
}
